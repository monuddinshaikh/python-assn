# Creating variables in Python

# Integer variable
age = 21
print("Age:", age)

# Float variable
height = 5.9
print("Height:", height)

# String variable
name = "moin"
print("Name:", name)

# Boolean variable
is_student = True
print("Is Student:", is_student)

# Multiple variables in one line
x, y, z = 10, 20, 30
print("x:", x, "y:", y, "z:", z)

# Assigning same value to multiple variables
a = b = c = "Python"
print("a:", a, "b:", b, "c:", c)
