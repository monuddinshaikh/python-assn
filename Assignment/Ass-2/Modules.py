import math

# Input number
num = float(input("Enter a number: "))

# Using math functions
square_root = math.sqrt(num)
ceiling_value = math.ceil(num)
floor_value = math.floor(num)

# Display results
print("Square Root:", square_root)
print("Ceil Value:", ceiling_value)
print("Floor Value:", floor_value)

# import random module
import random

random_int = random.randint(1,100)
random_float = random.random()
random_range = random.uniform(10,50)

# Display result.
print("Random Integer (1 to 100): ", random_int)
print("Random float (0 to 1): ", random_float)
print("Random float (10 to 50): ", random_range)
